#include <iostream>

using namespace std;

int main()
{
    int x,y;
    float a,b,c,d;
    x = 2;
    y = 4;
    a = x + y;
    b = x - y;
    c = x * y;
    d = x / y;
    cout << "a = " << a <<endl;
    cout << "b = " << b <<endl;
    cout << "c = " << c <<endl;
    cout << "d = " << d <<endl;
}
